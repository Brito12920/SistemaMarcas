    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       LogoNew log = new LogoNew();
       
       LogoSincron5 marca = new LogoSincron5();
       
     
       
        String pw = tf_pw.getText();
        String LL = tf_ll.getText();
        String linha1 = tf_inicio.getText();
        String linha2 = tf_linha.getText();
        int coluna = 0;
        int quantLinha = 0;
        int valorColuna = 0;
        if (tf_quantCol.getText() != null) {
            coluna = Integer.parseInt(tf_quantCol.getText());
        }

        if (tf_linhasImpressas.getText() != null) {
            quantLinha = Integer.parseInt(tf_linhasImpressas.getText());
        }

        if (tf_espCol.getText() != null) {
            valorColuna = Integer.parseInt(tf_espCol.getText());
        }

            String marc = null;

        switch (jComboBox1.getSelectedIndex()) {

            case 0:
                marc = marca.gerarMarca(log.getLogoSincron5(), pw, LL, linha1, linha2, coluna, quantLinha, valorColuna);
                break;
            case 1:
                marc = marca.gerarMarca(log.getLogoSincron10(), pw, LL, linha1, linha2, coluna, quantLinha, valorColuna);
                break;
            case 2:
                marc = marca.gerarMarca(log.getLogoSincron15(), pw, LL, linha1, linha2, coluna, quantLinha, valorColuna);
                break;

            case 3:
                marc = marca.gerarMarca(log.getLogoSincron20(), pw, LL, linha1, linha2, coluna, quantLinha, valorColuna);
                break;
            case 4:
                marc = marca.gerarMarca(log.getLogoSincron25(), pw, LL, linha1, linha2, coluna, quantLinha, valorColuna);
                break;

        }
             

       
      
        System.out.println(marc);
    }//GEN-LAST:event_jButton1ActionPerformed

